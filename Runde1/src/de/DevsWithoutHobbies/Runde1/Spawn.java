package de.DevsWithoutHobbies.Runde1;

class Spawn {
    private int x;
    private int y;
    private int z;

    Spawn(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
